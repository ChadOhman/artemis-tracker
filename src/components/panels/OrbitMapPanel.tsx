          ictx.arc(moonCx + moonR * 0.2, moonCy + moonR * 0.3, moonR * 0.08, 0, Math.PI * 2);
          ictx.fill();
          ictx.restore();

          ictx.save();
          ictx.font = "bold 10px monospace";
          ictx.fillStyle = "rgba(180,185,190,0.9)";
          ictx.textAlign = "center";
          ictx.fillText("Moon", moonCx, moonCy + moonR + 12);
          ictx.restore();

