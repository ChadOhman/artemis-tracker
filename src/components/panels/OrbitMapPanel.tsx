          ictx.beginPath();
          ictx.arc(moonCx, moonCy, moonR, 0, Math.PI * 2);
          ictx.fillStyle = mGrad;
          ictx.fill();
          ictx.fillStyle = "rgba(0,0,0,0.2)";
          ictx.beginPath();
          ictx.arc(moonCx - moonR * 0.3, moonCy - moonR * 0.2, moonR * 0.12, 0, Math.PI * 2);
          ictx.fill();
