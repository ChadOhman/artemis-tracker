type TranslationTree = typeof TRANSLATIONS.en;

// eslint-disable-next-line @typescript-eslint/no-explicit-any
export function getNestedValue(obj: any, key: string): string | undefined {
  const parts = key.split(".");
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  let current: any = obj;
  for (const part of parts) {
    if (current == null || typeof current !== "object") return undefined;
    current = current[part];
  }
  return typeof current === "string" ? current : undefined;
}

export function createTranslator(locale: Locale) {
  return function t(key: string): string {
    const localeTree = TRANSLATIONS[locale] as TranslationTree;
    const enTree = TRANSLATIONS.en as TranslationTree;

    const localeValue = getNestedValue(localeTree, key);
    if (localeValue !== undefined) return localeValue;

    // Fallback to English
    const enValue = getNestedValue(enTree, key);
    if (enValue !== undefined) return enValue;

    // Fallback to raw key
    return key;
  };
}
